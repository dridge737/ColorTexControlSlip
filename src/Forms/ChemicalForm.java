    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:
        if(ChemicalText.getText().length()>0 && !ChemicalText.getText().equals("Name :"))
        {
            thisChemical.setChemicalName(this.ChemicalText.getText());
            ChemicalHandler thisChemicalHandler = new ChemicalHandler();
            if(AddButton.getText().equals("Save"))
            {
               if(this.thisChemical.getChemicalId() != -1)
               {
                   thisChemicalHandler.UpdateChemical(thisChemical); 
                   thisChemical.setChemicalName("");
                   thisChemical.setChemicalId(-1);
                   this.DeleteButton.setEnabled(true);
                   this.EditButton.setText("Edit");
                   AddButton.setText("Add");
               }
            }
            else
            {
                if(thisChemicalHandler.AddNewChemical(thisChemical)){
                    JOptionPane.showMessageDialog(null, "Successfully added Chemical : "+ChemicalText.getText());
                    this.dispose();
                }
                else if(ChemicalText.getText() == defaultTextBoxString)
                {
                    JOptionPane.showMessageDialog(null, "Please input a text.");
                }
                else
                    JOptionPane.showMessageDialog(null, "Chemical with the same name has already been added.");
            }
            
            this.GetUpdatedTable();
            UpdateRowFilter("");
            this.ResetColorText();
        }
        else
        JOptionPane.showMessageDialog(null, "Please add a character/letter to the Chemical name");
            
    }//GEN-LAST:event_AddButtonActionPerformed

    public void ResetColorText()
    {
        this.ChemicalText.setText(defaultTextBoxString);
        ChemicalText.setForeground(new Color(204,204,204));
    }
    
    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        if(ChemicalTable.getSelectedRowCount() > 0 )
        {
            int CloseorNoreply = JOptionPane.showConfirmDialog(null,"Delete this Chemical? "
                  ,"Delete Chemical?", JOptionPane.YES_NO_OPTION);
            if(CloseorNoreply == JOptionPane.YES_OPTION)
            {
                String ChemicalName = this.ChemicalTable.getModel().getValueAt(this.ChemicalTable.getSelectedRow(), 0).toString();
                thisChemical.setChemicalName(ChemicalName);
                thisChemical.setChemicalId(new ChemicalHandler().GetChemicalIDFromChemicalName(ChemicalName));
                new ChemicalHandler().DeleteChemical(thisChemical.getChemicalId());
                this.GetUpdatedTable();
            }
        }else
        {
            JOptionPane.showMessageDialog(null, "Please select an Item in the table to be edited");
        }
        
    }//GEN-LAST:event_DeleteButtonActionPerformed

    private void GetUpdatedTable()
    {
        model = getUpdatedColorTableModel();
        this.ChemicalTable.setModel(model);
    }
    
    private void UpdateRowFilter(String row_filter_text)
    {
        TableRowSorter<TableModel> rowSorter
            = new TableRowSorter<>(this.ChemicalTable.getModel());
        
        this.ChemicalTable.setRowSorter(rowSorter);
        
        if (row_filter_text.trim().length() == 0) {
            rowSorter.setRowFilter(null);
        } else {
            rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + row_filter_text));        
        }
    }
    
